CREATE TABLE Users (
    id SERIAL PRIMARY KEY,
    gender VARCHAR(10),
    name_title VARCHAR(50),
    name_first VARCHAR(50),
    name_last VARCHAR(50),
    street_number INT,
    street_name VARCHAR(100),
    city VARCHAR(100),
    state VARCHAR(100),
    country VARCHAR(100),
    postcode VARCHAR(20),
    latitude VARCHAR(50),
    longitude VARCHAR(50),
    timezone_offset VARCHAR(10),
    timezone_description VARCHAR(100),
    email VARCHAR(100),
    login_uuid VARCHAR(50),
    login_username VARCHAR(50),
    login_password VARCHAR(100),
    login_salt VARCHAR(100),
    login_md5 VARCHAR(100),
    login_sha1 VARCHAR(100),
    login_sha256 VARCHAR(100),
    dob_date TIMESTAMPTZ,
    dob_age INT,
    registered_date TIMESTAMPTZ,
    registered_age INT,
    phone VARCHAR(50),
    cell VARCHAR(50),
    id_name VARCHAR(50),
    id_value VARCHAR(50),
    picture_large VARCHAR(200),
    picture_medium VARCHAR(200),
    picture_thumbnail VARCHAR(200),
    nat VARCHAR(10),
    seed VARCHAR(50),
    results_count INT,
    page INT,
    version VARCHAR(10)
);
